let currentLanguage = "zh-CN";

export function setLanguage(lang) {
  if (translations[lang]) {
    currentLanguage = lang;
    return true;
  }
  return false;
}

export function getLanguage() {
  return currentLanguage;
}

export function t(key) {
  const langTranslations = translations[currentLanguage] || translations["zh-CN"];
  return langTranslations[key] || translations["zh-CN"][key] || key;
}

export function updatePageI18n() {
  const lang = currentLanguage;
  const dict = translations[lang] || translations["zh-CN"];

  document.title = dict.appTitle;

  const i18nElements = document.querySelectorAll("[data-i18n]");
  i18nElements.forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      if (el.tagName === "LABEL" && el.querySelector('input[type="radio"], input[type="checkbox"]')) {
        const input = el.querySelector('input[type="radio"], input[type="checkbox"]');
        if (input) {
          el.childNodes.forEach((node) => {
            if (node.nodeType === Node.TEXT_NODE) {
              node.textContent = " " + dict[key];
            }
          });
        }
      } else {
        el.textContent = dict[key];
      }
    }
  });

  const i18nTitles = document.querySelectorAll("[data-i18n-title]");
  i18nTitles.forEach((el) => {
    const key = el.getAttribute("data-i18n-title");
    if (dict[key]) {
      el.setAttribute("data-tooltip", dict[key]);
    }
  });

  const i18nPlaceholders = document.querySelectorAll("[data-i18n-placeholder]");
  i18nPlaceholders.forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key]) {
      el.placeholder = dict[key];
    }
  });
}
